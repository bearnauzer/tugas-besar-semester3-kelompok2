    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:

    if (selectedKetahanan == null) {
        JOptionPane.showMessageDialog(this,
            "Pilih ketahanan terlebih dahulu",
            "Peringatan",
            JOptionPane.WARNING_MESSAGE);
        return;
    }

    int pilihan = JOptionPane.showOptionDialog(
        this,
        "Tambah parfum lagi atau langsung bayar?",
        "Konfirmasi",
        JOptionPane.YES_NO_OPTION,
        JOptionPane.QUESTION_MESSAGE,
        null,
        new Object[]{"Tambah Parfum", "Bayar Sekarang"},
        "Tambah Parfum"
    );

    totalHarga += hargaProduk + hargaKetahanan;

    daftarParfum.append("- Anak | ")
        .append(selectedCollection)
        .append(" | ")
        .append(selectedKetahanan)
        .append("\n");

    selectedCollection = null;
    selectedKetahanan = null;
    hargaProduk = 0;
    hargaKetahanan = 0;

    if (pilihan == 0) {
        CardLayout parentCL = (CardLayout) parentPanel.getLayout();
        parentCL.show(parentPanel, "card2"); // PanelAnak

        CardLayout anakCL = (CardLayout) PanelAnak.getLayout();
        anakCL.show(PanelAnak, "anakProduk");

    } else {
        lblTotalHarga.setText(
            "Total Pembayaran : Rp " + formatRupiah(totalHarga)
        );

        CardLayout parentCL = (CardLayout) parentPanel.getLayout();
        parentCL.show(parentPanel, "card4"); // PanelPria

        CardLayout priaCL = (CardLayout) PanelPria.getLayout();
        priaCL.show(PanelPria, "card6"); // Pembayaran
    }

    }//GEN-LAST:event_jButton9ActionPerformed

