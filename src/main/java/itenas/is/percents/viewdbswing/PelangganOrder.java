    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:                                        
    if (selectedKetahanan == null) {
        JOptionPane.showMessageDialog(this,
            "Pilih tingkat ketahanan terlebih dahulu",
            "Peringatan",
            JOptionPane.WARNING_MESSAGE);
        return;
    }

    int pilihan = JOptionPane.showOptionDialog(
        this,
        "Tambah parfum lagi atau langsung bayar?",
        "Konfirmasi",
        JOptionPane.YES_NO_OPTION,
        JOptionPane.QUESTION_MESSAGE,
        null,
        new Object[]{"Tambah Parfum", "Bayar Sekarang"},
        "Tambah Parfum"
    );

    totalHarga += hargaProduk + hargaKetahanan;

    daftarParfum.append("- ")
        .append(kategori)
        .append(" | ")
        .append(selectedCollection != null ? selectedCollection : "Custom")
        .append(" | ")
        .append(selectedKetahanan)
        .append("\n");

    selectedCollection = null;
    selectedKetahanan = null;
    hargaProduk = 0;
    hargaKetahanan = 0;

    if (pilihan == 0) {

    // ===== TAMBAH PARFUM =====
    CardLayout parentCL = (CardLayout) parentPanel.getLayout();

    if ("Wanita".equals(kategori)) {
        parentCL.show(parentPanel, "card3"); // PanelWanita
        CardLayout wanitaCL = (CardLayout) PanelWanita.getLayout();
        wanitaCL.show(PanelWanita, "card2"); // menu wanita
    } else {
        parentCL.show(parentPanel, "card4"); // PanelPria
        CardLayout priaCL = (CardLayout) PanelPria.getLayout();
        priaCL.show(PanelPria, "card2"); // menu pria
    }

} else {

    // ===== BAYAR SEKARANG =====
    lblTotalHarga.setText(
        "Total Pembayaran : Rp " + formatRupiah(totalHarga)
    );

    CardLayout parentCL = (CardLayout) parentPanel.getLayout();
    parentCL.show(parentPanel, "card4"); // selalu ke PanelPria

    CardLayout priaCL = (CardLayout) PanelPria.getLayout();
    priaCL.show(PanelPria, "card6"); // PembayaranPanel
}

    }//GEN-LAST:event_jButton4ActionPerformed

