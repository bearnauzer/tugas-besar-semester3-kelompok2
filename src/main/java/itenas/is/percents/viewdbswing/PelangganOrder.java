private void handleCustomWanitaSelection(javax.swing.JCheckBox clickedBox) {
    int selectedCount = 0;

    javax.swing.JCheckBox[] boxes = {
        cbFreshCleanW,
        cbFloralSoftW,
        cbSweetWarmW,
        cbSweetFloralW,
        cbCleanWoodyW,
        cbDeepSignatureW
    };

    for (javax.swing.JCheckBox box : boxes) {
        if (box.isSelected()) {
            selectedCount++;
        }
    }

    if (selectedCount > 3) {
        clickedBox.setSelected(false);
        JOptionPane.showMessageDialog(
            this,
            "Maksimal 3 aroma untuk Custom Perfume",
            "Batas Pilihan",
            JOptionPane.WARNING_MESSAGE
        );
    }
}


